<template>
  <el-container>
    <el-aside>
      配置s
    </el-aside>
    <el-main>
      <chart :options="option"></chart>
    </el-main>
  </el-container>
</template>

<script>
import 'echarts/lib/chart/bar'
// built-in theme
import 'echarts/theme/dark'
// custom theme
// import theme from './theme.json'
export default {
  name: 'charts',
  data () {
    let data = []

    for (let i = 0; i <= 360; i++) {
      let t = i / 180 * Math.PI
      let r = Math.sin(2 * t) * Math.cos(2 * t)
      data.push([r, i])
    }
    return {
      msg: 'Welcome to Your Vue.js App',
      option: {
        title: {
          text: '极坐标双数值轴'
        },
        legend: {
          data: ['line']
        },
        polar: {
          center: ['50%', '54%']
        },
        tooltip: {
          trigger: 'axis',
          axisPointer: {
            type: 'cross'
          }
        },
        angleAxis: {
          type: 'value',
          startAngle: 0
        },
        radiusAxis: {
          min: 0
        },
        series: [
          {
            coordinateSystem: 'polar',
            name: 'line',
            type: 'line',
            showSymbol: false,
            data: data
          }
        ],
        animationDuration: 2000
      }
    }
  }
}
</script>

